#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

UPDATE_COUNTS_STR="${UPDATE_COUNTS:-500 1000 2000 4000}"
VARIANTS_STR="${VARIANTS:-pretrained_finetune}"

NUM_ENVS="${NUM_ENVS:-4096}"
MAX_ITERATIONS="${MAX_ITERATIONS:-1024}"
SEEDS="${SEEDS:-2026}"

PROJECT_NAME="${PROJECT_NAME:-G1-Imitation-RLOpt-Pretrain}"
GROUP_NAME="${GROUP_NAME:-g1_bilinear_sr_pretrain_update_sweep_feature_only_4096_100m}"
RUN_PREFIX_BASE="${RUN_PREFIX_BASE:-g1_bilinear_sr_pretrain_update_sweep_feature_only_4096_100m}"

OFFLINE_BATCH_SIZE="${OFFLINE_BATCH_SIZE:-8192}"
OFFLINE_LOG_INTERVAL="${OFFLINE_LOG_INTERVAL:-100}"
OFFLINE_POLICY_BC_UPDATES="${OFFLINE_POLICY_BC_UPDATES:-2000}"
OFFLINE_POLICY_BC_BATCH_SIZE="${OFFLINE_POLICY_BC_BATCH_SIZE:-8192}"
ONLINE_SR_UPDATE_STEPS="${ONLINE_SR_UPDATE_STEPS:-8}"
SR_BATCH_SIZE="${SR_BATCH_SIZE:-4096}"
SAMPLE_EVAL_INTERVAL="${SAMPLE_EVAL_INTERVAL:-100}"

DRY_RUN="${DRY_RUN:-0}"
VIDEO="${VIDEO:-1}"
VIDEO_LENGTH="${VIDEO_LENGTH:-200}"
VIDEO_INTERVAL="${VIDEO_INTERVAL:-2000}"
CLUSTER_PROFILE="${CLUSTER_PROFILE:-}"

read -r -a UPDATE_COUNTS <<< "$UPDATE_COUNTS_STR"

echo "[INFO] update_counts='${UPDATE_COUNTS_STR}', variants='${VARIANTS_STR}', seeds='${SEEDS}', group='${GROUP_NAME}', dry_run='${DRY_RUN}'"

for updates in "${UPDATE_COUNTS[@]}"; do
    echo
    echo "[INFO] Submitting pretrain update-count sweep point: OFFLINE_NUM_UPDATES=${updates}"
    TASK="${TASK:-Isaac-Imitation-G1-v0}" \
    NUM_ENVS="$NUM_ENVS" \
    MAX_ITERATIONS="$MAX_ITERATIONS" \
    SEEDS="$SEEDS" \
    VARIANTS="$VARIANTS_STR" \
    PROJECT_NAME="$PROJECT_NAME" \
    GROUP_NAME="$GROUP_NAME" \
    RUN_PREFIX="${RUN_PREFIX_BASE}_u${updates}" \
    OFFLINE_NUM_UPDATES="$updates" \
    OFFLINE_BATCH_SIZE="$OFFLINE_BATCH_SIZE" \
    OFFLINE_LOG_INTERVAL="$OFFLINE_LOG_INTERVAL" \
    OFFLINE_POLICY_BC_UPDATES="$OFFLINE_POLICY_BC_UPDATES" \
    OFFLINE_POLICY_BC_BATCH_SIZE="$OFFLINE_POLICY_BC_BATCH_SIZE" \
    ONLINE_SR_UPDATE_STEPS="$ONLINE_SR_UPDATE_STEPS" \
    SR_BATCH_SIZE="$SR_BATCH_SIZE" \
    SAMPLE_EVAL_INTERVAL="$SAMPLE_EVAL_INTERVAL" \
    DRY_RUN="$DRY_RUN" \
    VIDEO="$VIDEO" \
    VIDEO_LENGTH="$VIDEO_LENGTH" \
    VIDEO_INTERVAL="$VIDEO_INTERVAL" \
    CLUSTER_PROFILE="$CLUSTER_PROFILE" \
    "$SCRIPT_DIR/submit_cluster_ablation.sh"
done

echo
echo "[INFO] Submitted all requested pretrain update-count sweep jobs."
